GtkWidget *help;

void menu_help(GtkWidget *mainmenu, GtkAccelGroup *accel_group) {
	GtkWidget *menu, *about;

	menu = gtk_menu_new();
	help = gtk_menu_item_new_with_mnemonic("_Help");

	gtk_menu_item_set_submenu(GTK_MENU_ITEM(help), menu);
	gtk_menu_shell_append(GTK_MENU_SHELL(mainmenu), help);

	about = gtk_image_menu_item_new_with_mnemonic("_About");

	gw_image_from_inline(about, about_icon_inline);

	gtk_menu_shell_append(GTK_MENU_SHELL(menu), about);

	g_signal_connect(G_OBJECT(about), "activate", G_CALLBACK(help_about), NULL);
}
void menu_help_hide(void) {
	gtk_widget_hide(help);
}
void menu_help_show(void) {
	gtk_widget_show(help);
}
